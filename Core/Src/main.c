int main(void)
{
  /* USER CODE BEGIN 1 */
  uint32_t last_time = 0;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  MX_TIM5_Init();
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 2 */

  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);

  // encoders for speed measurement
  HAL_TIM_Encoder_Start(&htim4, TIM_CHANNEL_ALL);
  HAL_TIM_Encoder_Start(&htim5, TIM_CHANNEL_ALL);

  // start system time
  __HAL_TIM_CLEAR_FLAG(
      &htim1, TIM_SR_UIF); // clear the flag for first interrupt not to happen
  HAL_TIM_Base_Start_IT(&htim1);

  init_system_timer(TIM1);
  // initialize two motors
  init_motor_action(&motor, &(TIM2->CCR1), &(TIM2->CCR2), &(TIM3->CCR1),
                    &(TIM3->CCR2));

  init_pi(&pid1);
  init_pi(&pid2);

  init_rpm(&rpm1);
  init_rpm(&rpm2);

  // for UART over USB we do not have any huart pointer or use any buffer
  init_communication_uart(&uart, NULL, NULL, 0);
  install_k_callback(&uart, &set_k_coeff);
  install_p_callback(&uart, &set_p_coeff);
  install_custom_callback(&uart, &send_data_over_UART);
  uart.received_sp = &set_sp;

  last_time = 0;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1) {
    uart_runloop_handler();
    // HAL_IWDG_Refresh(&hiwdg);
    struct val_time_t time = get_current_time();
    if (time.ms - last_time > 1000) {
      last_time = time.ms;
      HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);
      verify_pid_reset_cond();
      // send_data_over_UART(11);
    }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

